// __ validArgument() __________________________________________________________
// =============================================================================
void	ParsingArg::validArgument(char **arg)
{
	int	i;
	int	j;
	
	i =-1;
	while (arg[++i])
	{
		j = -1;
		if (arg[i][0] == '+')
			j = 0;
		while (arg[i][++j])
			if (!std::isdigit(arg[i][j]))
				throw MyException("Error");
	}
}
